/************************************************
 * マクロ部
 ************************************************/
#include <stdio.h>
#include <stdlib.h>
#include <stdbool.h>
#include <math.h>

/************************************************
 * 構造体宣言部
 ************************************************/

/************************************************
 * グローバル変数
 ************************************************/
char buf[128];  // 標準入力保存

/************************************************
 * プロトタイプ宣言部
 ************************************************/
 // 値の更新
void update(int *p, int i);

/************************************************
 * 関数部
 ************************************************/
int main(int argc, char const *argv[]) {
    int x = 0, i;
    // 標準入力受け取り
    while (fgets(buf, sizeof(buf), stdin) != NULL) {
        sscanf(buf, "%d", &i);  // 分割
        update(&x, i);  // 更新
        printf("%d\n", x);  // 表示
    }
    return 0;
}

// 値の更新
void update(int *p, int i) {
    if(*p + i >= 100 || *p + i <= -100) {
        *p = 0;
    } else {
        *p += i;
    }
}
