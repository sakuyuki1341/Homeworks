/************************************************
 * マクロ部
 ************************************************/
#include <stdio.h>
#include <stdlib.h>
#include <stdbool.h>
#include <math.h>

/************************************************
 * 構造体宣言部
 ************************************************/

/************************************************
 * グローバル変数
 ************************************************/
char buf[128];

/************************************************
 * プロトタイプ宣言部
 ************************************************/
void update(int *p, int i);

/************************************************
 * 関数部
 ************************************************/
int main(int argc, char const *argv[]) {
    int x = 0, i;
    while (fgets(buf, sizeof(buf), stdin) != NULL) {
        sscanf(buf, "%d", &i);
        update(&x, i);
        printf("%d\n", x);
    }
    return 0;
}

void update(int *p, int i) {
    *p += i;
}
