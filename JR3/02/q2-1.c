#include <stdio.h>

/************************************************
 * プロトタイプ宣言部
 ************************************************/

/************************************************
 * 関数部
 ************************************************/
int main(int argc, char const *argv[]) {
    int n = 0;  // 入力保存用
    scanf("%d", &n);
    int fib[n+1];   // フィボナッチの余り配列
    fib[0] = 0; fib[1] = 1;
    // n番目まで計算
    for(int i = 2; i <= n; i++) {
        fib[i] = fib[i-1] % 2718 + fib[i-2] % 2718;
    }
    printf("%d", fib[n] % 2718);    // 表示
    return 0;
}
