///
/// 文字列sを128進数とみなしてSIZEで割ったあまりを計算する関数
///
int hash(char *s) {
	int tmp = s[0] % SIZE;
	for(int i = 1; i < 32 && s[i] != '\0'; i++) {
		tmp = (tmp * 128 + s[i]) % SIZE;
	}
	return tmp;
}

///
/// メイン関数
///
int main() {
	char s[32];
	scanf("%[^\n]", s);
	printf("%d\n", hash(s));
	return 0;
}